const getNestedValue = (source, key) =>
  key.split('.').reduce((current, part) => (current && current[part] !== undefined ? current[part] : undefined), source)

const interpolate = (value, params = {}) => {
  if (typeof value !== 'string') return value
  return value.replace(/\{(\w+)\}/g, (_, name) => String(params[name] ?? `{${name}}`))
}

export const getDictionary = (language) => dictionaries[languages.includes(language) ? language : defaultLanguage]

export const translate = (language, key, params) => {
  const dictionary = getDictionary(language)
  const fallbackDictionary = getDictionary(defaultLanguage)
  const value = getNestedValue(dictionary, key) ?? getNestedValue(fallbackDictionary, key)
  return value === undefined ? key : interpolate(value, params)
}
